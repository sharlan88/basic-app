package by.itstep;

public class VarTester {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        System.out.println("My name is Alex");
        System.out.print("Current t = -2\n");
        System.out.print("Tomorrow t = -6\n");
        //variable declaration -
        //<type> <var_name>;
        int temp;
        double avg;

        //variable initialisation -
        //<var_name> = <value>;
        temp = -2;
        avg = 8.1;

        //variable declaration & initialisation in single line
        // <type> <var_name> = <value>;
        int age = 35;
        int h = 177;

        int nextTemp = -6;

        System.out.println(temp);
        System.out.println("Tomorrows temp is " + nextTemp);

        // <type> <var_name> = <value> ,... ;
        int a = 15, b = 30;

        int mathMark = 8, historyMark = 7;

        int x, z, u;

        System.out.println("Math: " + mathMark);
        System.out.println("History: " + historyMark);





    }

}
